<?php

declare(strict_types=1);

namespace LongTermSupport\OpenApiGenerator\Component\GeneratorCore;

use LongTermSupport\OpenApiGenerator\Component\GeneratorCore\Registry\Registry;
use PhpCsFixer\Console\Command\FixCommand;
use PhpCsFixer\ToolInfo;
use PhpParser\Node;
use PhpParser\Node\Stmt;
use PhpParser\PrettyPrinterAbstract;
use Symfony\Component\Console\Input\ArrayInput;
use Symfony\Component\Console\Output\NullOutput;
use Symfony\Component\Filesystem\Filesystem;

class Printer
{
    private bool $useFixer = false;

    private bool $cleanGenerated = true;

    public function __construct(
        private readonly PrettyPrinterAbstract $prettyPrinter,
        private readonly string $fixerConfig = '',
    ) {
    }

    public function setUseFixer(bool $useFixer): void
    {
        $this->useFixer = $useFixer;
    }

    public function setCleanGenerated(bool $cleanGenerated): void
    {
        $this->cleanGenerated = $cleanGenerated;
    }

    public function output(Registry $registry): void
    {
        if ($this->cleanGenerated) {
            $fs = new Filesystem();
            foreach ($registry->getOutputDirectories() as $directory) {
                $fs->remove($directory);
                $fs->mkdir($directory);
            }
        }

        $declareStrictTypes = new Stmt\Declare_([
            new Stmt\DeclareDeclare(new Node\Identifier('strict_types'), new Node\Scalar\Int_(1)),
        ]);

        foreach ($registry->getSchemas() as $schema) {
            foreach ($schema->getFiles() as $file) {
                if (!file_exists(\dirname($file->getFilename()))) {
                    \Safe\mkdir(\dirname($file->getFilename()), 0o755, true);
                }

                $stmts = [$declareStrictTypes, $file->getNode()];

                \Safe\file_put_contents(
                    $file->getFilename(),
                    $this->addGeneratedHeader($this->prettyPrinter->prettyPrintFile($stmts)),
                );
            }
        }

        if ($this->useFixer) {
            foreach ($registry->getOutputDirectories() as $directory) {
                $this->fix($directory);
            }
        }
    }

    protected function getDefaultRules(): string
    {
        $rules = [
            '@Symfony'             => true,
            'self_accessor'        => true,
            'array_syntax'         => ['syntax' => 'short'],
            'concat_space'         => ['spacing' => 'one'],
            'declare_strict_types' => true,
            'header_comment'       => [
                'header' => 'This file is auto-generated by PHP OpenAPI Generator.' . "\n" . 'DO NOT EDIT MANUALLY — changes will be overwritten when the generator is re-run.',
            ],
        ];

        if (version_compare(\PhpCsFixer\Console\Application::VERSION, '3.0.0', '>=')) {
            $rules['yoda_style'] = false;
        } elseif (version_compare(\PhpCsFixer\Console\Application::VERSION, '2.6.0', '>=')) {
            $rules['yoda_style'] = null;
        }

        return \Safe\json_encode($rules);
    }

    protected function fix(string $path): void
    {
        if (!class_exists(FixCommand::class)) {
            return;
        }

        $command = new FixCommand(new ToolInfo());
        $config  = [
            'path' => [$path],
        ];

        if ('' !== $this->fixerConfig) {
            $config['--config'] = $this->fixerConfig;
        } else {
            $config['--allow-risky'] = 'yes';
            $config['--rules']       = $this->getDefaultRules();
        }

        $command->run(new ArrayInput($config, $command->getDefinition()), new NullOutput());
    }

    /**
     * Prepend a generated-file header immediately after the opening <?php tag.
     * This runs unconditionally so the header is present whether or not php-cs-fixer is used.
     */
    private function addGeneratedHeader(string $fileContent): string
    {
        $header = <<<'HEADER'
            /*
             * This file is auto-generated by PHP OpenAPI Generator.
             * DO NOT EDIT MANUALLY — changes will be overwritten when the generator is re-run.
             */

            HEADER;

        // prettyPrintFile() always begins with "<?php\n\n"
        if (str_starts_with($fileContent, '<?php')) {
            return '<?php' . "\n\n" . $header . ltrim(substr($fileContent, 5));
        }

        return $header . $fileContent;
    }
}
